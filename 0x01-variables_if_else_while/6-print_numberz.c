#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: Prints all single-digit numbers of base 10, starting from 0,
 *              using the putchar function.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	putchar('0' + 0);
	putchar('1' + 0);
	putchar('2' + 0);
	putchar('3' + 0);
	putchar('4' + 0);
	putchar('5' + 0);
	putchar('6' + 0);
	putchar('7' + 0);
	putchar('8' + 0);
	putchar('9' + 0);
	putchar('\n');

	return (0);
}
